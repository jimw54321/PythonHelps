from cardcollection import CardCollection

####################################################################################

class Hand(CardCollection):

    """
    the cards held by a player
    """

    ################################################################################

    def __init__(self):
        super().__init__()

    ################################################################################

    def has2Clubs(self):

        found = False

        for c in self.cards:
            if str(c) == '2C':
                found = True
                break

        return found

    ################################################################################

